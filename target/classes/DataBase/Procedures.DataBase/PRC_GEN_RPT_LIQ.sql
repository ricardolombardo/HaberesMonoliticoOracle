CREATE OR REPLACE PROCEDURE PRC_GEN_RPT_LIQ (
    p_anio_desde IN NUMBER,
    p_mes_desde IN NUMBER,
    p_anio_hasta IN NUMBER,
    p_mes_hasta IN NUMBER,
    o_cursor OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN o_cursor FOR
    SELECT 
        LIQ.ID,
        LIQ.DESCRIPCION,
        LIQ.ANIO,
        LIQ.MES,
        COUNT(DISTINCT T.ID) AS TABULADOS,
        SUM(TC.MONTO) AS MONTO
    FROM LIQUIDACION LIQ
    JOIN TABULADO T ON T.ID_LIQUIDACION = LIQ.ID
    JOIN TABULADO_CONCEPTO TC ON TC.ID_TABULADO = T.ID
    JOIN CONCEPTO C ON C.ID = TC.ID_CONCEPTO
    WHERE (LIQ.ANIO*12 + LIQ.MES) > (p_anio_desde*12 + p_mes_desde)
      AND (LIQ.ANIO*12 + LIQ.MES) < (p_anio_hasta*12 + p_mes_hasta)
      AND TC.SENTIDO = 'D'
    GROUP BY LIQ.ID, LIQ.DESCRIPCION, LIQ.ANIO, LIQ.MES
    ORDER BY LIQ.ID, LIQ.DESCRIPCION, LIQ.ANIO, LIQ.MES;
END;
